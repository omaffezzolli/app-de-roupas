package modelo;

public class Loja {
	
	public String calca;
	public String camisa;
	public String casaco;
	
	public Integer quantidadecalca;
	public Integer quantidadecamisa;
	public Integer quantidadecasaco;
	
	public int total;
	
	
	
	
	public Integer getQuantidadecalca() {
		return quantidadecalca;
	}
	public void setQuantidadecalca(Integer quantidadecalca) {
		this.quantidadecalca = quantidadecalca;
	}
	public Integer getQuantidadecamisa() {
		return quantidadecamisa;
	}
	public void setQuantidadecamisa(Integer quantidadecamisa) {
		this.quantidadecamisa = quantidadecamisa;
	}
	public Integer getQuantidadecasaco() {
		return quantidadecasaco;
	}
	public void setQuantidadecasaco(Integer quantidadecasaco) {
		this.quantidadecasaco = quantidadecasaco;
	}
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public String getCalca() {
		return calca;
	}
	public void setCalca(String calca) {
		this.calca = calca;
	}
	public String getCamisa() {
		return camisa;
	}
	public void setCamisa(String camisa) {
		this.camisa = camisa;
	}
	public String getCasaco() {
		return casaco;
	}
	public void setCasaco(String casaco) {
		this.casaco = casaco;
	}
	
	
	

}



